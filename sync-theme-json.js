const fs = require( 'fs' );
const path = require( 'path' );

const themeJsonPath = path.resolve( __dirname, 'theme.json' );
const outputScssPath = path.resolve( __dirname, 'src/scss/_theme-variables.scss' );

if ( ! fs.existsSync( themeJsonPath ) ) {
	console.error( 'theme.json not found at ' + themeJsonPath );
	process.exit( 1 );
}

const themeJson = JSON.parse( fs.readFileSync( themeJsonPath, 'utf8' ) );
const settings = themeJson.settings || {};

let scssContent = '// This file is automatically generated from theme.json. Do not edit manually.\n\n';

// Colors.
if ( settings.color && settings.color.palette ) {
	scssContent += '// Colors\n';
	settings.color.palette.forEach( ( item ) => {
		scssContent += `$${ item.slug }: ${ item.color };\n`;
	} );
	scssContent += '\n';
}

// Typography.
if ( settings.typography && settings.typography.fontSizes ) {
	scssContent += '// Typography\n';
	settings.typography.fontSizes.forEach( ( item ) => {
		scssContent += `$font-size-${ item.slug }: ${ item.size };\n`;
	} );
	scssContent += '\n';
}

// Spacing.
if ( settings.spacing && settings.spacing.spacingSizes ) {
	scssContent += '// Spacers\n';
	scssContent += '$spacers: (\n';
	settings.spacing.spacingSizes.forEach( ( item ) => {
		scssContent += `  '${ item.slug }': ${ item.size },\n`;
	} );
	scssContent += ');\n';
}

fs.writeFileSync( outputScssPath, scssContent );
console.log( 'Successfully generated ' + outputScssPath );
